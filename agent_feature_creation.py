def compute_cpi_mom(
    df_macro_raw: pd.DataFrame,
    df_cpi_index_raw: pd.DataFrame
) -> pd.DataFrame:
    """
    Computes the Month-over-Month (MoM) CPI inflation rate and merges it into
    the macro dataset.

    Equation: cpi_mom_t = (cpi_index_level_t / cpi_index_level_{t-1}) - 1

    Parameters
    ----------
    df_macro_raw : pd.DataFrame
        The primary macro dataset with a DatetimeIndex.
    df_cpi_index_raw : pd.DataFrame
        The CPI dataset containing 'cpi_index_level' with a DatetimeIndex.

    Returns
    -------
    pd.DataFrame
        A new dataframe containing the original macro data and the computed
        'cpi_mom'.

    Raises
    ------
    TypeError
        If inputs are not pandas DataFrames or lack a DatetimeIndex.
    ValueError
        If 'cpi_index_level' is missing from the CPI dataframe.
    """
    # ------------------------------------------------------------------
    # Input validation: type checks
    # ------------------------------------------------------------------
    if not isinstance(df_macro_raw, pd.DataFrame):
        raise TypeError(f"df_macro_raw must be a pandas DataFrame, got {type(df_macro_raw).__name__}.")

    if not isinstance(df_cpi_index_raw, pd.DataFrame):
        raise TypeError(f"df_cpi_index_raw must be a pandas DataFrame, got {type(df_cpi_index_raw).__name__}.")

    # ------------------------------------------------------------------
    # Input validation: index checks
    # ------------------------------------------------------------------
    if not isinstance(df_macro_raw.index, pd.DatetimeIndex):
        raise TypeError("df_macro_raw must have a DatetimeIndex.")

    if not isinstance(df_cpi_index_raw.index, pd.DatetimeIndex):
        raise TypeError("df_cpi_index_raw must have a DatetimeIndex.")

    # ------------------------------------------------------------------
    # Input validation: required columns
    # ------------------------------------------------------------------
    if 'cpi_index_level' not in df_cpi_index_raw.columns:
        raise ValueError("df_cpi_index_raw must contain the column 'cpi_index_level'.")

    # ------------------------------------------------------------------
    # Create deep copies to preserve immutability of inputs
    # ------------------------------------------------------------------
    df_macro_out: pd.DataFrame = df_macro_raw.copy(deep=True)
    df_cpi_calc: pd.DataFrame = df_cpi_index_raw.copy(deep=True)

    # ------------------------------------------------------------------
    # Sort the CPI dataframe chronologically to ensure accurate percentage
    # change calculations
    # ------------------------------------------------------------------
    df_cpi_calc = df_cpi_calc.sort_index()

    # ------------------------------------------------------------------
    # Compute the discrete periodic rate of change (MoM) without
    # forward-filling NaNs
    # ------------------------------------------------------------------
    cpi_mom_series: pd.Series = df_cpi_calc['cpi_index_level'].pct_change(periods=1, fill_method=None)

    # Rename the resulting series to match the target schema
    cpi_mom_series.name = 'cpi_mom'

    # ------------------------------------------------------------------
    # Merge the computed series into the macro dataframe using a left join
    # on the index to prevent data leakage or row duplication
    # ------------------------------------------------------------------
    df_macro_out = df_macro_out.join(cpi_mom_series, how='left')

    logger.info("Successfully computed and merged 'cpi_mom' into macro dataset.")

    return df_macro_out


