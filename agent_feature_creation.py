def compute_periodic_returns(df_total_return_raw: pd.DataFrame) -> pd.DataFrame:
    """
    Computes the discrete periodic returns for a panel of assets, strictly
    preventing cross-sectional leakage.

    Equation: r_{t,i} = (TR_{t,i} / TR_{t-1,i}) - 1

    Parameters
    ----------
    df_total_return_raw : pd.DataFrame
        The total return dataset with a MultiIndex ['date', 'ticker',
        'investment_universe'] and column 'total_return_index'.

    Returns
    -------
    pd.DataFrame
        A new dataframe containing the computed 'periodic_return'.

    Raises
    ------
    TypeError
        If input is not a DataFrame or lacks the required MultiIndex.
    ValueError
        If 'total_return_index' is missing or contains non-positive values.
    """
    # ------------------------------------------------------------------
    # Input validation: type checks
    # ------------------------------------------------------------------
    if not isinstance(df_total_return_raw, pd.DataFrame):
        raise TypeError(f"df_total_return_raw must be a pandas DataFrame, got {type(df_total_return_raw).__name__}.")

    # ------------------------------------------------------------------
    # Input validation: MultiIndex checks
    # ------------------------------------------------------------------
    if not isinstance(df_total_return_raw.index, pd.MultiIndex):
        raise TypeError("df_total_return_raw must have a MultiIndex.")

    required_levels: List[str] = ['date', 'ticker', 'investment_universe']

    for level in required_levels:
        if level not in df_total_return_raw.index.names:
            raise ValueError(f"MultiIndex must contain the level '{level}'.")

    # ------------------------------------------------------------------
    # Input validation: required columns and value constraints
    # ------------------------------------------------------------------
    if 'total_return_index' not in df_total_return_raw.columns:
        raise ValueError("df_total_return_raw must contain the column 'total_return_index'.")

    if (df_total_return_raw['total_return_index'] <= 0).any():
        raise ValueError("All values in 'total_return_index' must be strictly positive.")

    # ------------------------------------------------------------------
    # Create a deep copy to preserve immutability
    # ------------------------------------------------------------------
    df_out: pd.DataFrame = df_total_return_raw.copy(deep=True)

    # ------------------------------------------------------------------
    # Sort the MultiIndex first by the cross-sectional identifiers, then chronologically
    # ------------------------------------------------------------------
    df_out = df_out.sort_index(level=['ticker', 'investment_universe', 'date'])

    # ------------------------------------------------------------------
    # Group by the cross-sectional identifiers and compute the percentage
    # change chronologically
    # ------------------------------------------------------------------
    df_out['periodic_return'] = df_out.groupby(
        level=['ticker', 'investment_universe']
    )['total_return_index'].pct_change(periods=1, fill_method=None)

    logger.info("Successfully computed 'periodic_return' for panel data.")

    return df_out


def aggregate_daily_factors_to_monthly(
    df_benchmark_factors_raw: pd.DataFrame,
    is_rf_annualized: bool = True
) -> pd.DataFrame:
    """
    Aggregates daily factor returns and the risk-free rate into monthly
    geometric returns.

    Equation: R_{monthly} = \prod_{d=1}^{D} (1 + r_{daily, d}) - 1
    If is_rf_annualized is True, the daily rf is first de-annualized:
    rf_daily = rf_ann / 365.

    Parameters
    ----------
    df_benchmark_factors_raw : pd.DataFrame
        Daily dataset containing 'mkt_rf', 'smb', 'hml', 'rf'.
    is_rf_annualized : bool, optional
        Flag indicating if 'rf' is provided as an annualized yield. Defaults to True.

    Returns
    -------
    pd.DataFrame
        A new dataframe with a monthly (Month-End) DatetimeIndex containing
        compounded returns.

    Raises
    ------
    TypeError
        If input is not a DataFrame or lacks a DatetimeIndex.
    ValueError
        If required factor columns are missing.
    """
    # ------------------------------------------------------------------
    # Input validation: type and index checks
    # ------------------------------------------------------------------
    if not isinstance(df_benchmark_factors_raw, pd.DataFrame):
        raise TypeError(f"df_benchmark_factors_raw must be a pandas DataFrame, got {type(df_benchmark_factors_raw).__name__}.")

    if not isinstance(df_benchmark_factors_raw.index, pd.DatetimeIndex):
        raise TypeError("df_benchmark_factors_raw must have a DatetimeIndex.")

    # ------------------------------------------------------------------
    # Input validation: required columns
    # ------------------------------------------------------------------
    required_cols: List[str] = ['mkt_rf', 'smb', 'hml', 'rf']

    for col in required_cols:
        if col not in df_benchmark_factors_raw.columns:
            raise ValueError(f"df_benchmark_factors_raw must contain the column '{col}'.")

    # ------------------------------------------------------------------
    # Create a deep copy to preserve immutability
    # ------------------------------------------------------------------
    df_calc: pd.DataFrame = df_benchmark_factors_raw.copy(deep=True)

    # ------------------------------------------------------------------
    # Sort the dataframe chronologically
    # ------------------------------------------------------------------
    df_calc = df_calc.sort_index()

    # ------------------------------------------------------------------
    # De-annualize the risk-free rate if necessary
    # ------------------------------------------------------------------
    if is_rf_annualized:
        df_calc['rf'] = df_calc['rf'] / 365.0

    # ------------------------------------------------------------------
    # Add 1 to all daily returns to prepare for geometric compounding
    # ------------------------------------------------------------------
    df_gross: pd.DataFrame = df_calc[required_cols] + 1.0

    # ------------------------------------------------------------------
    # Group by Month-End ('ME') and calculate the product of the gross returns
    # ------------------------------------------------------------------
    df_monthly_gross: pd.DataFrame = df_gross.groupby(pd.Grouper(freq='ME')).prod()

    # ------------------------------------------------------------------
    # Subtract 1 to convert the compounded gross returns back to net periodic returns
    # ------------------------------------------------------------------
    df_monthly_net: pd.DataFrame = df_monthly_gross - 1.0

    logger.info("Successfully aggregated daily factors to monthly geometric returns.")

    return df_monthly_net


